package data;

/**
 *
 * @author Admin
 */
public class Experience extends Candidate{
    private int exlnYear;
    private String proSkill;

    public Experience(int exlnYear, String proSkill, int id, String firstName, String lastName, String birthDate, String address, String phone, String email, String candidateType) {
        super(id, firstName, lastName, birthDate, address, phone, email, candidateType);
        this.exlnYear = exlnYear;
        this.proSkill = proSkill;
    }
   
    

    public int getExlnYear() {
        return exlnYear;
    }

    public void setExlnYear(int exlnYear) {
        this.exlnYear = exlnYear;
    }

    public String getProSkill() {
        return proSkill;
    }

    public void setProSkill(String proSkill) {
        this.proSkill = proSkill;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getBirthDate() {
        return birthDate;
    }

    public void setBirthDate(String birthDate) {
        this.birthDate = birthDate;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getCandidateType() {
        return candidateType;
    }

    public void setCandidateType(String candidateType) {
        this.candidateType = candidateType;
    }
    
}
